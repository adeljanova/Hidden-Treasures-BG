void Application::run() { showMenu(); }

void Application::listVillages() const {
  std::cout << "\n--- Списък с населени места ---\n";
  for (size_t i = 0; i < villages.size(); ++i) {
    std::cout << i + 1 << ". с. " << villages[i].getName() << " (Област "
              << villages[i].getRegion() << ")\n";
  }
}

void Application::showVillageDetails() const {
  listVillages();
  std::cout << "\nИзберете номер на населено място за детайли (0 за отказ): ";
  size_t choice;

  if (std::cin >> choice && choice > 0 && choice <= villages.size()) {
    std::cout << "\n";
    villages[choice - 1].displayAllPlaces();
  }
}

void Application::addNewVillage() {
  std::string name, region;
  std::cout << "\nВъведете име на новото населено място: ";
  std::getline(std::cin >> std::ws, name);
  std::cout << "Въведете област: ";
  std::getline(std::cin >> std::ws, region);

  villages.push_back(Village(name, region));
  std::cout << "Успешно добавено населено място: " << name << "!\n";
}

void Application::addNewObject() {
  listVillages();
  if (villages.empty()) {
    std::cout << "Няма добавени населени места!\n";
    return;
  }

  std::cout
      << "\nИзберете номер на населено място, към което да добавите обект: ";
  size_t choice;
  if (!(std::cin >> choice) || choice == 0 || choice > villages.size()) {
    std::cout << "Невалиден избор.\n";
    std::cin.clear();
    std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
    return;
  }

  std::string name, desc, coords;
  std::cout << "Въведете име на обекта: ";
  std::getline(std::cin >> std::ws, name);
  std::cout << "Въведете описание: ";
  std::getline(std::cin >> std::ws, desc);
  std::cout << "Въведете координати (напр. 42.123, 23.456): ";
  std::getline(std::cin >> std::ws, coords);

  std::cout << "\nТип на обекта:\n1. Културен ресурс\n2. Природен ресурс\n3. "
               "Туристическа атракция\nИзбор: ";
  int typeChoice;
  if (!(std::cin >> typeChoice) || typeChoice < 1 || typeChoice > 3) {
    std::cout << "Невалиден тип.\n";
    std::cin.clear();
    std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
    return;
  }

  if (typeChoice == 1) {
    std::string century, cultureType;
    std::cout << "Въведете век: ";
    std::getline(std::cin >> std::ws, century);
    std::cout << "Въведете тип културен обект (Музей, Храм и др.): ";
    std::getline(std::cin >> std::ws, cultureType);
    villages[choice - 1].addPlace(std::make_unique<CulturalResource>(
        name, desc, coords, century, cultureType));
  } else if (typeChoice == 2) {
    std::string formation;
    int isProtected;
    std::cout << "Въведете тип природно образувание (Водопад, Пещера и др.): ";
    std::getline(std::cin >> std::ws, formation);
    std::cout << "Защитена територия ли е? (1 за Да, 0 за Не): ";
    std::cin >> isProtected;
    villages[choice - 1].addPlace(std::make_unique<NaturalResource>(
        name, desc, coords, formation, isProtected == 1));
  } else {
    double fee;
    int rating;
    std::cout << "Въведете входна такса (в лв.): ";
    std::cin >> fee;
    std::cout << "Въведете рейтинг (1-5 звезди): ";
    std::cin >> rating;
    villages[choice - 1].addPlace(
        std::make_unique<TouristAttraction>(name, desc, coords, fee, rating));
  }

  std::cout << "Успешно добавен нов обект към "
            << villages[choice - 1].getName() << "!\n";
}

void Application::showMenu() {
  int choice = 0;
  while (choice != 5) {
    std::cout << "\n========================================\n";
    std::cout << " Скритите Съкровища на България (Меню)\n";
    std::cout << "========================================\n";
    std::cout << "1. Разгледай всички населени места\n";
    std::cout << "2. Преглед на обекти в конкретно населено място\n";
    std::cout << "3. Добавяне на ново населено място\n";
    std::cout << "4. Добавяне на нов обект\n";
    std::cout << "5. Изход\n";
    std::cout << "Изберете опция: ";

    if (!(std::cin >> choice)) {
      std::cin.clear();
      std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
      continue;
    }

    switch (choice) {
    case 1:
      listVillages();
      break;
    case 2:
      showVillageDetails();
      break;
    case 3:
      addNewVillage();
      break;
    case 4:
      addNewObject();
      break;
    case 5:
      std::cout << "Довиждане! Благодарим, че избрахте Скритите Съкровища на "
                   "България!\n";
      break;
    default:
      std::cout << "Невалиден избор. Моля, опитайте отново.\n";
    }
  }
}
